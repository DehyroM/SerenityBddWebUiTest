package co.com.sofka.model.automationpractice;

public class ContactUsModel {

    private String emailContactUs;
    private String orderReference;
    private String messageContactUs;

    public String getEmailContactUs() {
        return emailContactUs;
    }

    public void setEmailContactUs(String emailContactUs) {
        this.emailContactUs = emailContactUs;
    }

    public String getOrderReference() {
        return orderReference;
    }

    public void setOrderReference(String orderReference) {
        this.orderReference = orderReference;
    }

    public String getMessageContactUs() {
        return messageContactUs;
    }

    public void setMessageContactUs(String messageContactUs) {
        this.messageContactUs = messageContactUs;
    }
}
